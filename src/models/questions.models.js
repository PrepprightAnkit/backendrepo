import mongoose from "mongoose";
const Schema = mongoose.Schema;

// Answer schema
const answerSchema = new Schema({
  answerText: {
    type: String,
    required: true,
  },
  likes: {
    type: Number,
    default: 0,
  },
  dislikes: {
    type: Number,
    default: 0,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },

});

// Question schema
const questionSchema = new Schema({
  questionText: {
    type: String,
    required: true,
  },
  likes: {
    type: Number,
    default: 0,
  },
  dislikes: {
    type: Number,
    default: 0,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },

  answers: [answerSchema], // Embed answers as a subdocument array
});



export const Question = mongoose.model('Question', questionSchema);